#include <gtest/gtest.h>

#include <CoreLib/Core_File.hpp>

#include <fstream>
#include <io.h>
#include <cstdio>

namespace text_formating
{
	TEST(core_file, safe_op)
	{
		core::file_read file;
		EXPECT_TRUE(file.eof());
		EXPECT_TRUE(file.error());
		EXPECT_FALSE(file.good());
		ASSERT_FALSE(file.is_open());
		file.close();
	}

}