#include <CoreLib/net/core_net_init.hpp>

#ifdef _WIN32
#	include <Winsock2.h>

/// \n
namespace core
{
bool Net_Init()
{
	WSADATA wsaData;
	// initiates use of WS2_32.DLL by a process (only Win32 platform)
	return WSAStartup(MAKEWORD(2,2), &wsaData) == 0;
}

void Net_End()
{
	WSACleanup();
}

} //namespace core

#endif
